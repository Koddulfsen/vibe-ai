    print(f"  â€¢ Implicit requirements: {len(understanding.implicit_requirements)}")
    print(f"  â€¢ Security requirements: {len(understanding.security_requirements)}")
    print(f"  â€¢ Architectural patterns: {len(understanding.architectural_patterns)}")
    
    # Show some implicit requirements
    print(f"\nğŸ” Sample implicit requirements:")
    for req in understanding.implicit_requirements[:5]:
        print(f"  â€¢ {req}")
    if len(understanding.implicit_requirements) > 5:
        print(f"  â€¢ ... and {len(understanding.implicit_requirements) - 5} more")
    
    # Phase 2: Analysis
    print("\n\nğŸ” Phase 2: Project Analysis...")
